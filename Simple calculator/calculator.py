class calculator:
    def __init__(self,x,y):
        self.x=x
        self.y=y
    def sum(self):
        final = self.x+self.y
        return final
    def sub(self):
        final = self.x-self.y
        return final

    def mul(self):
        final = self.x*self.y
        return final

    def div(self):
        final = self.x/self.y
        return final

